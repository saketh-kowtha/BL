class Stack {
    
    constructor(){
        this.list = []
    }

    push(ele){
       this.list.push(ele)
    }

    isEmpty(){
        return this.list.length == 0
    }

    size(){
        return this.list.length 
    }

    peek(){
        return this.list[this.list.length - 1]
    }

    pop(){
        this.list.pop()
    }

}


